.MODEL SMALL
.STACK 100H
.DATA

LESS  DB 0AH,0DH,"LESS$" 
GREATER  DB 0AH,0DH,"GREATER$"
EQUAL  DB 0AH,0DH,"EQUAL$"

.CODE

MAIN PROC
    
  MOV AX,@DATA 
  MOV DS,AX 
    
  MOV BH,5
  
  MOV AH,01H
  INT 21H
  MOV BL,AL 
  SUB BL,30H
  
  CMP BL,BH
  
  JG PUTG 
  JL PUT 
  JE PUTE
  
  JMP EXIT
  
  PUT:
  LEA DX,LESS 
  MOV AH,09H
  INT 21H  
  JMP EXIT  
  
  PUTG:
  LEA DX,GREATER 
  MOV AH,9
  INT 21H  
  JMP EXIT 
  
  PUTE:
  LEA DX,EQUAL 
  MOV AH,9
  INT 21H  
  JMP EXIT 
  
EXIT:
    MOV AH,4CH
    INT 21H
    
    MAIN ENDP
END MAIN
